<h1><abbr title="Frequently Asked Questions">FAQ</abbr></h1>
<p>Here's some frequently asked questions about this site.</p>

<h2>What is this?</h2>
<p>This site is a web-based chat, primarily for <a href="http://groups.google.com/group/mediatechnologie">mediatechnology</a> students at the <a href="http://ma-web.nl">Mediacollege Amsterdam</a>.</p>

<h2>What commands can I use in the chat?</h2>
<p>Some available /commands are:</p>
<ul>
	<li>String functions: /rev, /rot13</li>
	<li>Cryptographic functions: /md5, /sha1</li>
	<li>Status functions: /uname</li>
</ul>
<p>If you'd like to see additional functions, request them to an operator.</p>

<h2>How's this thing made?</h2>
<p>This chat is built on a <a href="http://php.net">PHP</a> and <a href="http://mysql.com">MySQL</a> backend, with an HTML/CSS and <a href="http://jquery.com">jQuery</a> frontend.</p>
<p>The code is available on <a href="http://github.com/duckson/chat">github</a>.</p>

